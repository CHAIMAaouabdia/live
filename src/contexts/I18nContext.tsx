interface I18nContextValue {
  lang: Lang;
  setLang: (l: Lang) => void;
  t: (key: string) => string;
  dir: "ltr" | "rtl";
  locale: string;
}

const I18nContext = createContext<I18nContextValue | null>(null);

export const I18nProvider = ({ children }: { children: ReactNode }) => {
  const [lang, setLangState] = useState<Lang>(() => {
    const saved = typeof window !== "undefined" ? (localStorage.getItem("lm-lang") as Lang | null) : null;
    return saved ?? "FR";
  });

  const setLang = (l: Lang) => {
    setLangState(l);
    localStorage.setItem("lm-lang", l);
  };

  useEffect(() => {
    document.documentElement.lang = lang.toLowerCase();
    document.documentElement.dir = lang === "AR" ? "rtl" : "ltr";
  }, [lang]);

  const t = (key: string) => translations[lang][key] ?? translations.FR[key] ?? key;
  const dir = lang === "AR" ? "rtl" : "ltr";
  const locale = lang === "EN" ? "en-US" : lang === "AR" ? "ar-DZ" : "fr-FR";

  return <I18nContext.Provider value={{ lang, setLang, t, dir, locale }}>{children}</I18nContext.Provider>;
};

export const useI18n = () => {
  const ctx = useContext(I18nContext);
  if (!ctx) throw new Error("useI18n must be used inside I18nProvider");
  return ctx;
};
